package com.aiqcomponents.sample;

public class Region {

    private String name;
    private String acronym;

    public Region(String name, String acronym) {
        this.name = name;
        this.acronym = acronym;
    }

    public String getName() {
        return name;
    }

    public String getAcronym() {
        return acronym;
    }
}
